def decrypt_rot13(name)
  decrypt = name.chars.map do |char|
    case char
    when 'a'..'m', 'A'..'M'
      (char.ord + 13).chr
    when 'n'..'z', 'N'..'Z'
      (char.ord - 13).chr
    else
      char
    end
  end
  decrypt.join
end

